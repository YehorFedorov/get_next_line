#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	const char	ch = c;
	int			i;

	i = 0;
	while (s[i] != ch)
	{
		if (s[i] == '\0')
			return (0);
		i++;
	}
	return (char*)(s + i);
}
